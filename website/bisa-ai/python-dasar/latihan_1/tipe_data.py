# tipe data integer
angka_bulat = 250
print(angka_bulat)

# tipe data float
angka_decimal = 11.333
print(angka_decimal)

# complex_number
angka_complex = 1 + 4j
print(angka_complex)

# string
string_tunggal = 'ini adalah tipe data string kutip tunggal'
string_double = "ini adalah tipe data string kutip double"

print(string_tunggal)
print(string_double)

# string multi line
string_multiple = '''
ini adalah tipe data
string multiple line
bisa lebih dari satu
baris.
'''

print(string_multiple)

# string concatenation
string_concat = "satu" + " dua"
print(string_concat)

# tipe data list []
contoh_list = ['a', 1, 2, 3, 'b']
print(contoh_list)

# tipe data tuple ()
contoh_tuple = ('a', 1, 2, 3, 'b')
print(contoh_tuple)

# tipe data set {}
contoh_set = {'a', 1, 2, 3, 'b'}
print(contoh_set)

# tipe data dictionary mirip seperti object pada javascript
contoh_dictionary = {
    "name": "Tanga Ibuki",
    "age": 11,
    "Club": "Pandemonium Society",
    "Academy": "Gehenna Academy",
}

print(contoh_dictionary)

# latihan
list_data = ['kucing', 4, 'burung', 2, 'ular', 5.7, 3, 'kelinci']
print(list_data[:4])