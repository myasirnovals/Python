first = "Hello"
second = "World"

# Concatenation
result = first + " " + second
print(result)

# Slicing
text = "Hello World!"
print(text[0:5])
print(text[-11])

# Formatting
name = "John"
age = 30
print(f"My name is {name} and I'm {age} years old.")

# Split method
sentence = "Hello World!"
print(sentence.split("o"))

# Join method
words = ["Hello", "World"]
print("-".join(words))

# Replace method
text = "Hello Java!"
print(text.replace("Java", "Python"))

# Strip method
text = "   Hello World!   "
print(text)
print(text.strip())